#Números inteiros e decimais (float)

idade = 35
peso = 70.5

print(idade)
print(peso)

#Strings

nome = "João da Silva"
frase = "Olá! Tudo bem com você?"

print(nome)
print(frase)

#tipo booleano
esta_chovendo = True
tem_sol = False

print("Está chovendo?", esta_chovendo)
print("Tem sol?", tem_sol)

#Atualizando variáveis
idade = 20
print("Idade inicial:", idade)

idade = idade + 1
print("Depois do aniversário:", idade)

#Operações matemáticas
x = 10
y = 4

soma = x + y
subtracao = x - y
multiplicacao = x * y
divisao = x / y

print(soma)
print(subtracao)
print(multiplicacao)
print(divisao)

#Concatenação de Strings

nome = "José"
sobrenome = "de Carvalho"

nome_completo = nome + " " + sobrenome
print("Nome Completo:", nome_completo)

#Formatação de Strings

nome = "Beatriz"
idade = 29

print(f"Olá, meu nome é {nome} e tenho {idade} anos de idade")

